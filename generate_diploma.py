def write_section_1_2(doc):
    add_heading_sub(doc, '1.2. Машинное обучение и свёрточные нейронные сети')

    add_body_text(doc,
        'Машинное обучение (Machine Learning) — раздел искусственного '
        'интеллекта, изучающий методы построения алгоритмов, способных '
        'обучаться на данных и улучшать свою производительность по мере '
        'накопления опыта. В контексте классификации изображений '
        'машинное обучение позволяет автоматически выявлять закономерности '
        'в визуальных данных и использовать их для отнесения новых '
        'изображений к заданным категориям.'
    )

    add_body_text(doc,
        'Глубокое обучение (Deep Learning) — подраздел машинного обучения, '
        'использующий искусственные нейронные сети с большим числом слоёв '
        'для автоматического извлечения иерархических признаков из данных. '
        'В отличие от классических методов, требующих ручного '
        'конструирования признаков (feature engineering), глубокие '
        'нейронные сети самостоятельно обучаются выделять информативные '
        'признаки непосредственно из сырых пикселей изображения.'
    )

    add_body_text(doc,
        'Свёрточная нейронная сеть (Convolutional Neural Network, CNN) — '
        'специализированная архитектура нейронной сети, разработанная '
        'для обработки данных с сетчатой структурой. Основу CNN '
        'составляют три типа слоёв. Свёрточные слои (convolutional layers) '
        'выполняют операцию свёртки входных данных с набором обучаемых '
        'фильтров (ядер), формируя карты признаков (feature maps). '
        'Слои подвыборки (pooling layers) уменьшают пространственную '
        'размерность карт признаков, снижая вычислительную нагрузку '
        'и обеспечивая локальную инвариантность к сдвигам. Полносвязные '
        'слои (fully connected layers) осуществляют итоговую '
        'классификацию на основе извлечённых признаков.'
    )

    add_figure(doc, 2, 'Архитектура свёрточной нейронной сети',
              image_path=_fig_path(2))

    add_body_text(doc,
        'Процесс обучения CNN заключается в минимизации функции потерь '
        '(loss function) методом обратного распространения ошибки '
        '(backpropagation) с использованием алгоритмов градиентного '
        'спуска. Наиболее распространёнными оптимизаторами являются '
        'стохастический градиентный спуск (SGD), Adam (Adaptive Moment '
        'Estimation) и RMSprop. Для задач многоклассовой классификации '
        'стандартной функцией потерь является категориальная '
        'кросс-энтропия (Cross-Entropy Loss).'
    )

    add_body_text(doc,
        'Для оценки качества классификации используются стандартные '
        'метрики: accuracy (общая доля правильных ответов), precision '
        '(точность — доля истинно положительных среди всех положительных '
        'предсказаний), recall (полнота — доля обнаруженных положительных '
        'примеров из всех действительно положительных) и F1-score '
        '(гармоническое среднее precision и recall). В задачах с '
        'несбалансированными классами применяются macro- и weighted-'
        'среднее указанных метрик, а также матрица ошибок (confusion '
        'matrix).'
    )

    add_body_text(doc,
        'Важным аспектом обучения нейронных сетей является проблема '
        'переобучения (overfitting), при котором модель запоминает '
        'обучающие примеры, но теряет способность к обобщению на '
        'новых данных. Для борьбы с переобучением применяются методы '
        'регуляризации: Dropout (случайное отключение нейронов), '
        'аугментация данных (искусственное расширение обучающей выборки), '
        'ранняя остановка (early stopping), а также L2-регуляризация '
        'весов. Кроме того, существенную роль играет правильный выбор '
        'гиперпараметров: скорости обучения (learning rate), размера '
        'мини-пакета (batch size) и количества эпох обучения.'
    )

    add_table_caption(doc, 1, 'Основные метрики качества классификации')
    make_table(doc,
        ['Метрика', 'Формула', 'Описание'],
        [
            ['Accuracy', 'TP+TN / (TP+TN+FP+FN)', 'Общая доля верных ответов'],
            ['Precision', 'TP / (TP + FP)', 'Доля верных среди предсказанных'],
            ['Recall', 'TP / (TP + FN)', 'Доля найденных из всех верных'],
            ['F1-score', '2·P·R / (P + R)', 'Гармоническое среднее P и R'],
        ]
    )


