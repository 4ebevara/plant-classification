        '        super().__init__()\n'
        '        self.backbone = models.efficientnet_b0(\n'
        '            weights=EfficientNet_B0_Weights.IMAGENET1K_V1)\n'
        '        if freeze_backbone:\n'
        '            for p in self.backbone.features.parameters():\n'
        '                p.requires_grad = False\n'
