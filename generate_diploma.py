        'визуальных данных. Цифровое изображение представляет собой '
        'двумерную матрицу пикселей, каждый из которых описывается '
        'набором числовых значений. Для цветных изображений в формате '
        'RGB каждый пиксель характеризуется тремя каналами: красным (R), '
        'зелёным (G) и синим (B), принимающими значения от 0 до 255. '
